import pg from 'pg';
import crypto from 'crypto';
const { Pool } = pg;

// –ì–µ–Ω–µ—Ä–∞—Ü–∏—è —É–Ω–∏–∫–∞–ª—å–Ω–æ–≥–æ –∏–º–µ–Ω–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
function generateUsername() {
  const timestamp = Date.now().toString().slice(-6);
  return `test_user_${timestamp}`;
}

// –ì–µ–Ω–µ—Ä–∞—Ü–∏—è —É–Ω–∏–∫–∞–ª—å–Ω–æ–≥–æ —Ä–µ—Ñ–µ—Ä–∞–ª—å–Ω–æ–≥–æ –∫–æ–¥–∞
function generateRefCode() {
  return crypto.randomBytes(4).toString('hex').toUpperCase();
}

// –°–æ–∑–¥–∞–Ω–∏–µ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è –∫ –±–∞–∑–µ –¥–∞–Ω–Ω—ã—Ö
async function createDbConnection() {
  console.log('üîÑ –ü–æ–¥–∫–ª—é—á–µ–Ω–∏–µ –∫ Neon DB...');
  
  const pool = new Pool({
    connectionString: process.env.DATABASE_URL,
    ssl: {
      rejectUnauthorized: false
    }
  });
  
  return pool;
}

// –°–æ–∑–¥–∞–Ω–∏–µ —Ç–µ—Å—Ç–æ–≤–æ–≥–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
async function createTestUser(pool) {
  console.log('üîÑ –°–æ–∑–¥–∞–Ω–∏–µ —Ç–µ—Å—Ç–æ–≤–æ–≥–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è...');
  
  const username = generateUsername();
  const refCode = generateRefCode();
  const guestId = crypto.randomUUID(); // –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º —É–Ω–∏–∫–∞–ª—å–Ω—ã–π guest_id
  const now = new Date().toISOString();
  
  try {
    // –°–æ–∑–¥–∞–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —Å —É—á–µ—Ç–æ–º —Ä–µ–∞–ª—å–Ω–æ–π —Å—Ç—Ä—É–∫—Ç—É—Ä—ã —Ç–∞–±–ª–∏—Ü—ã
    // –°–Ω–∞—á–∞–ª–∞ –ø—Ä–æ–≤–µ—Ä–∏–º –º–∞–∫—Å–∏–º–∞–ª—å–Ω—ã–π ID, —á—Ç–æ–±—ã –Ω–µ –±—ã–ª–æ –∫–æ–Ω—Ñ–ª–∏–∫—Ç–∞
    const maxIdResult = await pool.query('SELECT MAX(id) FROM users');
    const nextId = maxIdResult.rows[0].max + 1 || 1000; // –ò—Å–ø–æ–ª—å–∑—É–µ–º –±–æ–ª—å—à–æ–µ —á–∏—Å–ª–æ –µ—Å–ª–∏ —Ç–∞–±–ª–∏—Ü–∞ –ø—É—Å—Ç–∞—è
    
    const userResult = await pool.query(`
      INSERT INTO users (
        id,
        username, 
        telegram_id, 
        ref_code, 
        balance_uni,
        balance_ton,
        created_at,
        guest_id
      ) VALUES ($1, $2, $3, $4, $5, $6, $7, $8) RETURNING *
    `, [
      nextId,
      username,
      Math.floor(Math.random() * 1000000) + 100000, // —Å–ª—É—á–∞–π–Ω—ã–π telegram_id
      refCode,
      1000, // –Ω–∞—á–∞–ª—å–Ω—ã–π –±–∞–ª–∞–Ω—Å UNI
      5,    // –Ω–∞—á–∞–ª—å–Ω—ã–π –±–∞–ª–∞–Ω—Å TON
      now,
      guestId
    ]);
    
    const user = userResult.rows[0];
    console.log('‚úÖ –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å —É—Å–ø–µ—à–Ω–æ —Å–æ–∑–¥–∞–Ω:');
    console.log(`ID: ${user.id}`);
    console.log(`Username: ${user.username}`);
    console.log(`Ref Code: ${user.ref_code}`);
    console.log(`Guest ID: ${user.guest_id}`);
    console.log(`Balance UNI: ${user.balance_uni}`);
    console.log(`Balance TON: ${user.balance_ton}`);
    console.log(`Created At: ${user.created_at}`);
    
    return user;
  } catch (error) {
    console.error('‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è:', error.message);
    throw error;
  }
}

// –ü—Ä–æ–≤–µ—Ä–∫–∞ —Å—É—â–µ—Å—Ç–≤–æ–≤–∞–Ω–∏—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
